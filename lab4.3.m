t = 0:0.2:2*pi;
y1 = exp(-0.5*t);
y2 = cos(3*t);


figure;

%  plot()
plot(t, y1, 'y.', t, y2, 'm--'); % 'y.' - жовтий колір, точковий стиль для y1(t); 'm--' - рожевий колір, пунктирний стиль для y2(t)
legend('y1', 'y2');

hold on; % утримання графічного контексту

%  stem()
stem(t, y1, 'y'); % 'y' - жовтий колір
hold on;

% stairs()
stairs(t, y1, 'g'); % 'g' - зелений колір

hold off; % відпуск графічного контексту

